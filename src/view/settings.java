    private void title(){
        String sql = "select * from app where id='1'";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                String name = hasil.getString("name");
                String version = hasil.getString("version");
                this.setTitle(name + " " + version);
            }
        } catch (Exception e) {
            this.setTitle("Warning : No Connection");
        }
    }
    
    public void dataApp(){
        String sql = "select * from app where id='1'";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {                
                String id = hasil.getString("id");
                String name = hasil.getString("name");
                String version = hasil.getString("version");
                String appdesc = hasil.getString("description");
                labelAppId.setText(id);
                txtAppName.setText(name);
                txtVersion.setText(version);
                txtDescriptionApp.setText(appdesc);
            }
            title();
        } catch (Exception e) {
        }
    }
    
    public void dataComp(){
        String sql = "select * from company where comp_id='1'";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {                
                String id = hasil.getString("comp_id");
                String name = hasil.getString("name");
                String address = hasil.getString("address");
                String description = hasil.getString("description");
                String trademark = hasil.getString("trademark");
                labelCompId.setText(id);
                txtCompName.setText(name);
                txtAddress.setText(address);
                txtDescriptionComp.setText(description);
                txtTrademark.setText(trademark);
            }
        } catch (Exception e) {
        }
    }
    
