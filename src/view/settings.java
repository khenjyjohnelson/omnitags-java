    private void btnChangeAppActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangeAppActionPerformed
        int ok = JOptionPane.showConfirmDialog(null, "Do you want to change data", "Confirmation Dialog", JOptionPane.YES_NO_OPTION);
        if (ok==0) {
            String sql = "update app set name=?, version=?, description=? where id = '1'";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);

            stat.setString(1, txtAppName.getText());
            stat.setString(2, txtVersion.getText());
            stat.setString(3, txtDescriptionApp.getText());
            stat.executeUpdate();

            JOptionPane.showMessageDialog(null, "Data Changed Succesfully");

            dataApp();
            txtAppName.requestFocus();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data Invalid" + e);
        }
        }
        
    }//GEN-LAST:event_btnChangeAppActionPerformed

